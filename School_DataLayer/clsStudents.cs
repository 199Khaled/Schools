        public static bool GetStudentsInfoByID(int? StudentID , ref int? PersonID, ref DateTime? EnrollmentDate)
{
    bool isFound = false;

    try
    {
        using (SqlConnection connection = new SqlConnection(clsDataAccessSettings.ConnectionString))
        {
            string query = "SP_Get_Students_ByID";

            using (SqlCommand command = new SqlCommand(query, connection))
            {
                command.CommandType = CommandType.StoredProcedure;

                // Ensure correct parameter assignment
                command.Parameters.AddWithValue("@StudentID", StudentID ?? (object)DBNull.Value);

                connection.Open();
                using (SqlDataReader reader = command.ExecuteReader())
                { 
                    if (reader.Read())
                    {
                        // The record was found
                        isFound = true;

                                PersonID = (int)reader["PersonID"];
                                EnrollmentDate = (DateTime)reader["EnrollmentDate"];

                    }
                }
            }
        }
    }
    catch (Exception ex)
    {
        // Handle all exceptions in a general way
        ErrorHandler.HandleException(ex, nameof(GetStudentsInfoByID), $"Parameter: StudentID = " + StudentID);
    }

    return isFound;
}

