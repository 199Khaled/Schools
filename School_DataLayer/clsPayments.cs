        public static bool GetPaymentsInfoByID(int? PaymentID , ref int? StudentID, ref decimal? Amount, ref DateTime? PaymentDate, ref string Status)
{
    bool isFound = false;

    try
    {
        using (SqlConnection connection = new SqlConnection(clsDataAccessSettings.ConnectionString))
        {
            string query = "SP_Get_Payments_ByID";

            using (SqlCommand command = new SqlCommand(query, connection))
            {
                command.CommandType = CommandType.StoredProcedure;

                // Ensure correct parameter assignment
                command.Parameters.AddWithValue("@PaymentID", PaymentID ?? (object)DBNull.Value);

                connection.Open();
                using (SqlDataReader reader = command.ExecuteReader())
                { 
                    if (reader.Read())
                    {
                        // The record was found
                        isFound = true;

                                StudentID = (int)reader["StudentID"];
                                Amount = (decimal)reader["Amount"];
                                PaymentDate = reader["PaymentDate"] != DBNull.Value ? (DateTime?)reader["PaymentDate"] : null;
                                Status = reader["Status"] != DBNull.Value ? reader["Status"].ToString() : null;

                    }
                }
            }
        }
    }
    catch (Exception ex)
    {
        // Handle all exceptions in a general way
        ErrorHandler.HandleException(ex, nameof(GetPaymentsInfoByID), $"Parameter: PaymentID = " + PaymentID);
    }

    return isFound;
}

