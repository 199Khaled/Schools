        public static bool GetClassroomsInfoByID(int? ClassroomID , ref string ClassroomName, ref int? Capacity, ref string Building)
{
    bool isFound = false;

    try
    {
        using (SqlConnection connection = new SqlConnection(clsDataAccessSettings.ConnectionString))
        {
            string query = "SP_Get_Classrooms_ByID";

            using (SqlCommand command = new SqlCommand(query, connection))
            {
                command.CommandType = CommandType.StoredProcedure;

                // Ensure correct parameter assignment
                command.Parameters.AddWithValue("@ClassroomID", ClassroomID ?? (object)DBNull.Value);

                connection.Open();
                using (SqlDataReader reader = command.ExecuteReader())
                { 
                    if (reader.Read())
                    {
                        // The record was found
                        isFound = true;

                                ClassroomName = reader["ClassroomName"] != DBNull.Value ? reader["ClassroomName"].ToString() : null;
                                Capacity = reader["Capacity"] != DBNull.Value ? (int?)reader["Capacity"] : null;
                                Building = reader["Building"] != DBNull.Value ? reader["Building"].ToString() : null;

                    }
                }
            }
        }
    }
    catch (Exception ex)
    {
        // Handle all exceptions in a general way
        ErrorHandler.HandleException(ex, nameof(GetClassroomsInfoByID), $"Parameter: ClassroomID = " + ClassroomID);
    }

    return isFound;
}

